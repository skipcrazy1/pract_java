package pract23;

public interface Queue {
    // Предусловие: очередь не пуста
    // Постусловие: возвращает первый элемент без удаления
    Object element();

    // Предусловие: есть место для нового элемента
    // Постусловие: элемент добавлен в конец очереди
    void enqueue(Object item);

    // Предусловие: очередь не пуста
    // Постусловие: первый элемент удален и возвращен
    Object dequeue();

    // Постусловие: возвращает текущий размер очереди
    int size();

    // Постусловие: возвращает true, если очередь пуста
    boolean isEmpty();

    // Постусловие: очередь становится пустой
    void clear();
}